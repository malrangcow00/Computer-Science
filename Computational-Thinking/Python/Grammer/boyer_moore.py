# 보이어-무어 알고리즘의 주요 아이디어는 다음과 같습니다:

# 오른쪽에서 왼쪽으로 검색한다: 보통 문자열 검색 알고리즘은 왼쪽에서 오른쪽으로 패턴을 대상 문자열과 비교합니다. 하지만 보이어-무어 알고리즘은 패턴의 끝 문자부터 시작하여 오른쪽에서 왼쪽으로 검색합니다.

# Bad character rule: 패턴의 오른쪽 끝 문자가 대상 문자열에서 일치하지 않을 경우, 이 불일치가 발생한 위치에서 패턴의 끝 문자가 대상 문자열의 어떤 문자와 일치하는지를 검사합니다. 이를 통해 패턴을 대상 문자열 상에서 더 빠르게 이동할 수 있습니다.

# Good suffix rule: 패턴 내에서 일치하지 않는 부분 문자열이 대상 문자열에 다른 위치에서 일치하는지를 검사하여 패턴을 더 빠르게 이동시키는 규칙입니다.

# 이러한 아이디어들을 활용하여 보이어-무어 알고리즘은 평균적으로 다른 문자열 검색 알고리즘보다 효율적으로 동작합니다. 특히 대상 문자열과 비교할 때 패턴의 끝 문자와 대상 문자열의 불일치한 문자가 발생하면, Bad character rule을 활용하여 패턴을 더 빠르게 이동시킵니다. 또한 Good suffix rule을 사용하여 패턴 내에서 일치하지 않는 부분 문자열을 대상 문자열 어느 부분과 일치하는지 검사하여 패턴의 이동을 최적화합니다.

# 보이어-무어 알고리즘은 특히 패턴이 길고 대상 문자열이 큰 경우에 효과적이며, 대규모 텍스트 처리나 검색 엔진 등에서 널리 사용되어 왔습니다.